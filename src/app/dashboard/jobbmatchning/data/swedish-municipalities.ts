// Haversine-avstånd i km mellan två punkter.
export function distanceKm(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 6371;
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = Math.sin(dLat/2)**2 + Math.cos(lat1*Math.PI/180)*Math.cos(lat2*Math.PI/180)*Math.sin(dLon/2)**2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
}

// Hitta användarens ort (fuzzy på namn) + de N geografiskt närmaste orterna.
// Returnerar [användarens ort, ...närmaste] om orten hittas, annars [].
export function nearbyMunicipalities(userLocation: string | null | undefined, count = 6): SwedishMunicipality[] {
  if (!userLocation) return [];
  const norm = (s: string) => s.toLowerCase().trim();
  const target = SWEDISH_MUNICIPALITIES.find(m => norm(m.name) === norm(userLocation))
    || SWEDISH_MUNICIPALITIES.find(m => norm(userLocation).includes(norm(m.name)) || norm(m.name).includes(norm(userLocation)));
  if (!target) return [];
  const rest = SWEDISH_MUNICIPALITIES
    .filter(m => m.conceptId !== target.conceptId)
    .map(m => ({ m, d: distanceKm(target.lat, target.lon, m.lat, m.lon) }))
    .sort((a, b) => a.d - b.d)
    .slice(0, count)
    .map(x => x.m);
  return [target, ...rest];
}
